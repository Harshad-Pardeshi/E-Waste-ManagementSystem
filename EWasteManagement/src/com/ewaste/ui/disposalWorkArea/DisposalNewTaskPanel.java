    private void populateRequestTable() {
        DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();

        model.setRowCount(0);

        for (WorkRequest request : disposalOrganization.getWorkQueue().getWorkRequestList()) {
            Object[] row = new Object[5];
            row[0] = request.getProduct();
            row[1] = request;
            row[2] = request.getSender().getPerson().getFirstName();
            row[3] = request.getReceiver() == null ? null : request.getReceiver().getPerson().getFirstName();
            row[4] = request.getStatus();

            model.addRow(row);
        }
    }

