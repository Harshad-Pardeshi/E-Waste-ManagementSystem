    private void populateVo(Properties props) {
        Product product = new Product();
        product.setProductName(props.get("productName").toString());
        product.setProductManufacturer(props.get("productManufacturer").toString());
        product.setProducDescription(props.get("producDescription").toString());
        product.setProductFeatures(props.get("productFeatures").toString());
        product.setSellingPrice(Double.valueOf(props.get("sellingPrice").toString()));
        //productVo.setCategory(props.getProperty("category").);
        product.setDateOfManufacturer(Util.getDate(props.get("dateOfManufacturer").toString()));
        product.setQuantity(Double.valueOf(props.get("quantity").toString()));
        System.out.println(product);

        SortingWorkRequest request = new SortingWorkRequest();
        request.setProduct(product);
        request.setSender(userAccount);
        request.setStatus(Constant.SENT_TO_SORTER);

        Organization org = null;
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (organization instanceof SortingOrganization) {
                org = organization;
                break;
            }
        }
        if (org != null) {
            org.getWorkQueue().getWorkRequestList().add(request);
            userAccount.getWorkQueue().getWorkRequestList().add(request);
        }

    }
