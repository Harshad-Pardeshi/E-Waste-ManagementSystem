    private void btnSellActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSellActionPerformed
        boolean valid = true;
        String manufacturer = txtManufacturer.getText();
        String productName = txtName.getText();
        String sellingPrice = txtSellingPrice.getText();
        String quantity = spnQuantity.getValue().toString();
        String features = txtFeatures.getText();
        String desc = txtDescription.getText();

        String[] attr = new String[4];

        attr[0] = manufacturer;
        attr[1] = productName;
        attr[2] = features;
        attr[3] = desc;

        for (String val : attr) {
            if (val == "null" || val.isEmpty() || val.trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Kindly enter information marked with * before submitting", null, JOptionPane.WARNING_MESSAGE);
                valid = false;
                break;
            }
        }

        if (valid) {

            Product product = new Product();
            product.setProductManufacturer(manufacturer);
            product.setProductName(productName);
            if (!quantity.equals("0")) {
                product.setSellingPrice(Double.parseDouble(sellingPrice));
            } else {
                product.setSellingPrice(0);
            }

            if (!quantity.equals("0")) {
                product.setQuantity(Double.parseDouble(quantity));
            } else {
                product.setQuantity(0);
            }

            product.setCategory((String)cmbCategory.getSelectedItem());
            String strDate = cmbDateChooser.getText();
            DateFormat df = new SimpleDateFormat("dd/MM/yyyy");
            Date manufacturingDate = null;
            try {
                manufacturingDate = df.parse(strDate);
            } catch (ParseException e) {
                e.printStackTrace();
            }
            product.setDateOfManufacturer(manufacturingDate);

            SortingWorkRequest request = new SortingWorkRequest();
            request.setProduct(product);
            request.setSender(userAccount);
            request.setStatus(Constant.SENT_TO_SORTER);

            Organization org = null;
            for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                if (organization instanceof SortingOrganization) {
                    org = organization;
                    break;
                }
            }
            if (org != null) {
                org.getWorkQueue().getWorkRequestList().add(request);
                userAccount.getWorkQueue().getWorkRequestList().add(request);
            }
            JOptionPane.showMessageDialog(this, "Product requested to be recycled. \n We would get back to you once it is approved.");
            resetFields();
        }
    }//GEN-LAST:event_btnSellActionPerformed

