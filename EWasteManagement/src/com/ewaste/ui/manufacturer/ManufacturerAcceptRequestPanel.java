private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();
        
        model.setRowCount(0);
        lblCount.setText(String.valueOf(receiverOrganization.getWorkQueue().getWorkRequestList().size())); 
        for (WorkRequest request : receiverOrganization.getWorkQueue().getWorkRequestList()){
                Object[] row = new Object[4];
                row[0] = request;
                row[1] = request.getMessage();
                row[2] = request.getProduct().getQuantity();
                row[3] = request.getStatus();
                //String result = ((LabTestWorkRequest) request).getTestResult();
                //row[3] = result == null ? "Waiting" : result;
                model.addRow(row);
        }
    }
