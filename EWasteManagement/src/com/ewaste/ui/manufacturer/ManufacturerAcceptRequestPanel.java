    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        int requestNumber = workRequestJTable.getSelectedRow();
        if(requestNumber<0){
            JOptionPane.showMessageDialog(this, "Please Select a row");
        }
        else{
            WorkRequest workRequest = (WorkRequest)workRequestJTable.getValueAt(requestNumber, 0);
            AccountantWorkRequest request = new AccountantWorkRequest();
            request.setProduct(workRequest.getProduct());
            request.setSender(userAccount);
            request.setStatus(Constant.SENT_TO_ACCOUNT);
            //set the price to be returned
            
            Organization org = null;
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                if (enterprise instanceof OtherRecyclerEnterprise) {
                    for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                        if (organization instanceof ReceiverOrganization) {
                            org = organization;
                            break;
                        }
                    }
                }
            }
            if (org!=null){
                workRequest.setStatus(Constant.COMPLETE);
                org.getWorkQueue().getWorkRequestList().add(request);
                userAccount.getWorkQueue().getWorkRequestList().add(request);
            }
            JOptionPane.showMessageDialog(this, "Product accepted and Money sent to recycler");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

